func (self *Country) Find(code string) body.Body {

	response := body.Json()

	found := self.Collection.Find(
		db.Cond{
			"code": strings.ToUpper(code),
		},
	)

	if found == nil {
		response.Set(
			sugar.Tuple {
				"error": "Not found",
			},
		)
	} else {
		response.Set(
			sugar.Tuple {
				"success": "Found",
				"data": found,
			},
		)
	}

	return response
}

